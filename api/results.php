if(total > 10000){
  Swal.fire({title: 'Reduce number of cards to <10K', icon: 'warning', showConfirmButton: false, toast: true, position: 'top-end', timer: 3000});
  return false;
}

$('.carregadas').text(total);
$('.btn-play').attr('disabled', true);
$('.btn-stop').attr('disabled', false);

line.forEach(function(data){
var callBack = $.ajax({
    url: gate + '?lista=' + data + '&sec=' + sec + '&cst=' + cst + '&unm=' + unm + '&idd=' + idd,
    success: function(retorno){
        if(retorno.indexOf("CHARGED") >= 0){
            $('#lista_charge').append(retorno);
            removelinha();
            charge = charge +1;
            }
            else if(retorno.indexOf("LIVE") >= 0){
            $('#lista_LIVEs').append(retorno);
            removelinha();
            live = live +1;
            }
            else if(retorno.indexOf("CCN") >= 0){
            $('#lista_aprovadas').append(retorno);
            removelinha();
            lives = lives +1;
            }else{
            $('#lista_reprovadas').append(retorno);
            removelinha();
            dies = dies +1;
        }
        testadas = charge + live + lives + dies;
        $('.charge').text(charge);
        $('.LIVEs').text(live);
        $('.aprovadas').text(lives);
        $('.reprovadas').text(dies);
        $('.testadas').text(testadas);
        
        if(testadas == total){
            Swal.fire({title: 'All cards has been checked', icon: 'success', showConfirmButton: false, toast: true, position: 'top-end', timer: 3000});
            $('.btn-play').attr('disabled', false);
            $('.btn-stop').attr('disabled', true);
        }
        }
      });
      $('.btn-stop').click(function(){
      Swal.fire({title: 'Paused', icon: 'warning', showConfirmButton: false, toast: true, position: 'top-end', timer: 3000});
      $('.btn-play').attr('disabled', false);
      $('.btn-stop').attr('disabled', true);      
        callBack.abort();
        return false;
      });
    });
  });
});
  
function removelinha() {
var lines = $('.form-checker').val().split('\n');
lines.splice(0, 1);
$('.form-checker').val(lines.join("\n"));
}               
 
