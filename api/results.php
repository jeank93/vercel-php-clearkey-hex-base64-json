    Swal.fire({title: 'No card found!', icon: 'error', showConfirmButton: false, toast: true, position: 'top-end', timer: 3000});
    return false;
}

Swal.fire({title: 'Cards are being checked', icon: 'success', showConfirmButton: false, toast: true, position: 'top-end', timer: 3000});

var line = array.filter(function(value){
if(value.trim() !== ""){
    txt += value.trim() + '\n';
    return value.trim();
}
});

/*
var line = array.filter(function(value){
return(value.trim() !== "");
});
*/

var total = line.length;


/*
line.forEach(function(value){
txt += value + '\n';
});
*/

